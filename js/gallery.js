let currentProjectImages = [];
let currentIndex = 0;

document.addEventListener("DOMContentLoaded", () => {
  const lightbox = document.getElementById("lightbox");
  const lightboxImg = document.getElementById("lightbox-img");
  const captionText = document.getElementById("caption");
  const closeBtn = document.querySelector(".close");
  const prevBtn = document.querySelector(".prev");
  const nextBtn = document.querySelector(".next");

  // --- GALÉRIA MEGNYITÁSA ---

  // Event Delegation a gombokra ("Teljes sorozat")
  document.addEventListener("click", (e) => {
    // Ha a "Teljes sorozat" gombra kattintunk
    if (
      e.target.classList.contains("cta-gomb") &&
      e.target.hasAttribute("data-project")
    ) {
      e.preventDefault();
      const projectId = e.target.getAttribute("data-project");

      if (projectImages[projectId]) {
        currentProjectImages = projectImages[projectId];
        currentIndex = 0;
        openModal();
      } else {
        console.error("Nincs ilyen projekt definiálva: " + projectId);
      }
    }
    // Ha a képre kattintunk (opcionális: ez is megnyithatja a galériát)
    else if (e.target.tagName === "IMG" && e.target.closest(".portfolio-img")) {
      // Megkeressük a hozzá tartozó gombot, hogy tudjuk melyik projekt
      const article = e.target.closest(".portfolio-item");
      const btn = article.querySelector(".cta-gomb");
      if (btn && btn.hasAttribute("data-project")) {
        const projectId = btn.getAttribute("data-project");
        if (projectImages[projectId]) {
          currentProjectImages = projectImages[projectId];
          currentIndex = 0; // Vagy megkereshetnénk, hogy ez hanyadik kép, de most egyszerűsítünk
          openModal();
        }
      }
    }
  });

  function openModal() {
    lightbox.style.display = "block";
    updateImage();
  }

  function updateImage() {
    lightboxImg.src = currentProjectImages[currentIndex];
    // Képaláírás: "Kép X / Y"
    captionText.innerHTML = `Kép ${currentIndex + 1} / ${
      currentProjectImages.length
    }`;

    // Gombok láthatósága (opcionális: elrejthetnénk, ha csak 1 kép van)
    if (currentProjectImages.length <= 1) {
      prevBtn.style.display = "none";
      nextBtn.style.display = "none";
    } else {
      prevBtn.style.display = "block";
      nextBtn.style.display = "block";
    }
  }

  // --- NAVIGÁCIÓ ---

  // Globális függvények, hogy a HTML onclick attribútum elérje őket (ha nem addEventListener-t használunk)
  // De jobb az addEventListener, ezért itt definiáljuk őket és kötjük be.

  window.changeSlide = function (n) {
    currentIndex += n;

    // Körkörös lapozás
    if (currentIndex >= currentProjectImages.length) {
      currentIndex = 0;
    } else if (currentIndex < 0) {
      currentIndex = currentProjectImages.length - 1;
    }

    updateImage();
  };

  // Billentyűzet vezérlés (Nyilak)
  document.addEventListener("keydown", (e) => {
    if (lightbox.style.display === "block") {
      if (e.key === "ArrowLeft") {
        changeSlide(-1);
      } else if (e.key === "ArrowRight") {
        changeSlide(1);
      } else if (e.key === "Escape") {
        lightbox.style.display = "none";
      }
    }
  });

  // --- BEZÁRÁS ---

  if (closeBtn) {
    closeBtn.addEventListener("click", () => {
      lightbox.style.display = "none";
    });
  }

  window.addEventListener("click", (e) => {
    if (e.target === lightbox) {
      lightbox.style.display = "none";
    }
  });
});
